package com.crm.model;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;

/**
 * 
 * 营销计划表
 * @author 洪学超
 *
 */
@Entity
public class Sal_plan_t {
	/*
	 * 序号
	 */
	@Id
	@GeneratedValue
     private int plan_id;

	/*
 	 * 序号
 	 */
     private int plan_chc_id;
     
 	/*
 	 * 计划做成日期
 	 */
     private Date plan_date;
     
 	/*
 	 * 计划内容
 	 */
     private String plan_todo;
     
 	/*
 	 * 执行结果
 	 */
     private String plan_result;

	public int getPlan_id() {
		return plan_id;
	}

	public void setPlan_id(int plan_id) {
		this.plan_id = plan_id;
	}

	public int getPlan_chc_id() {
		return plan_chc_id;
	}

	public void setPlan_chc_id(int plan_chc_id) {
		this.plan_chc_id = plan_chc_id;
	}

	public Date getPlan_date() {
		return plan_date;
	}

	public void setPlan_date(Date plan_date) {
		this.plan_date = plan_date;
	}

	public String getPlan_todo() {
		return plan_todo;
	}

	public void setPlan_todo(String plan_todo) {
		this.plan_todo = plan_todo;
	}

	public String getPlan_result() {
		return plan_result;
	}

	public void setPlan_result(String plan_result) {
		this.plan_result = plan_result;
	}
     
     
}
