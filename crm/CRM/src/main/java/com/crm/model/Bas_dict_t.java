package com.crm.model;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;

/**
 * 
 * 基本数据字典表
 * @author 洪学超
 *
 */
@Entity
public class Bas_dict_t {
	
	/**
	 * 序号
	 */
	@Id
	@GeneratedValue
     private int dict_id;
 	/**
 	 * 数据类型
 	 */
     private String dict_type;
 	/**
 	 * 数据项名称
 	 */
     private String dict_item;
 	/**
 	 * 数据项值
 	 */
     private String dict_value;
 	/**
 	 * 可否编辑
 	 */
    private String dict_is_editable;
	public int getDict_id() {
		return dict_id;
	}
	public void setDict_id(int dict_id) {
		this.dict_id = dict_id;
	}
	public String getDict_type() {
		return dict_type;
	}
	public void setDict_type(String dict_type) {
		this.dict_type = dict_type;
	}
	public String getDict_item() {
		return dict_item;
	}
	public void setDict_item(String dict_item) {
		this.dict_item = dict_item;
	}
	public String getDict_value() {
		return dict_value;
	}
	public void setDict_value(String dict_value) {
		this.dict_value = dict_value;
	}
	public String getDict_is_editable() {
		return dict_is_editable;
	}
	public void setDict_is_editable(String dict_is_editable) {
		this.dict_is_editable = dict_is_editable;
	}
}
